public class Main {
    public static void main(String[] args) {
        double x = 0.1 + 0.2;
        System.out.println("x = " + x);
        System.out.println(x > 0.3);

        double y = 1.0 - 0.9;
        System.out.println("y = " + y);
        System.out.println(y < 0.1);

        double z = 0.2 + 0.7;
        System.out.println("z = " + z);
        System.out.println(z < 0.9);
    }
}
